% test of the LGL node set with exact coordinate
tol = 10e-10;

%% Test 1: N=3
r_ext = [-1;-0.447213595499958;0.447213595499958;1;-1.;...
    -0.333333333333333;0.447213595499958;-1;-0.447213595499958;-1];
s_ext = [-1;-1.;-1;-1;-0.447213595499958;-0.333333333333333;...
    -0.447213595499958;0.447213595499958;0.447213595499958;1];

% triangle shape
N = 3;
tri = StdRegions.Triangle(N);

for i = 1:tri.nNode
    assert( abs(tri.r(i) - r_ext(i)) <= tol)
    assert( abs(tri.s(i) - s_ext(i)) <= tol)
end

